# integer
print(10); # decimalism
print(0xa); # hexadecimal

# float
print(0.05); # normal
print(5e-2); # scentific

# string
print("I'm OK!") # normal
print("I\'m OK!") # escape character
print(r"\n\b\d\s") # real string

# boolean
print(True, False); # normal
print(3 > 2, 3 < 2); # boolean calc
print(3 > 2 and 1 < 2); # and
print(1 == 2 or 1 < 2); # or
print(not 1 > 2); # not

# None
print(None == 0) # None is not equal to 0

# divide
print(10 / 3) # result is a float
print(10 // 3) # 3 is a integer(floor)
print(99 // 5) # 19 is a integer(floor)
print(99 // 5.1) # 19.0 is a float(floor)
print(99 // 5.01) # 19.0 is a float(floor)
print(10 % 3) # 1